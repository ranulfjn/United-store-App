package com.isi.unitedstore;

import android.graphics.Bitmap;

public class cart {
    int id;
    String userName;
    Bitmap image;
    String name;
    int price;
    int qty;

    public cart(int id, String userName, Bitmap image, String name, int price, int qty) {
        this.id = id;
        this.userName = userName;
        this.image = image;
        this.name = name;
        this.price = price;
        this.qty = qty;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public Bitmap getImage() {
        return image;
    }

    public void setImage(Bitmap image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public int getQty() {
        return qty;
    }

    public void setQty(int qty) {
        this.qty = qty;
    }
}
